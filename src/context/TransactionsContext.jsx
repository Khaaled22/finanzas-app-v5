import React, { createContext, useContext, useState, useEffect, useCallback, useMemo, useRef } from 'react';
import StorageManager from '../modules/storage/StorageManager';
import { INITIAL_TRANSACTIONS } from '../config/initialData';
import { loadFromSupabase, saveToSupabase, mergeArrayById, filterActive, softDelete, deduplicateByContent } from '../modules/supabase/syncUtils';

const SYNC_KEY = 'transactions_v5';

const TransactionsContext = createContext();

export const useTransactions = () => {
  const context = useContext(TransactionsContext);
  if (!context) {
    throw new Error('useTransactions debe usarse dentro de TransactionsProvider');
  }
  return context;
};

export function TransactionsProvider({ children, currentUser }) {
  const [transactions, setTransactions] = useState(() =>
    deduplicateByContent(StorageManager.load(SYNC_KEY, INITIAL_TRANSACTIONS))
  );
  const syncReady = useRef(false);

  // Generate missing auto-instances for recurring transactions
  const generateRecurringInstances = useCallback((allTransactions) => {
    const templates = allTransactions.filter(tx => tx.recurrence === 'monthly' && tx.recurringId && tx.id === tx.recurringId);
    if (templates.length === 0) return [];

    const today = new Date();
    const currentYM = `${today.getFullYear()}-${String(today.getMonth() + 1).padStart(2, '0')}`;
    const toAdd = [];

    for (const template of templates) {
      const templateDate = new Date(template.date + 'T00:00:00');
      const templateDay = templateDate.getDate();
      let year = templateDate.getFullYear();
      let month = templateDate.getMonth() + 1;

      // Start from the month after the template
      month++;
      if (month > 12) { month = 1; year++; }

      while (true) {
        const ym = `${year}-${String(month).padStart(2, '0')}`;
        if (ym > currentYM) break;

        const exists = allTransactions.some(tx =>
          tx.recurringId === template.id &&
          tx.id !== template.id &&
          tx.date?.startsWith(ym)
        ) || toAdd.some(tx =>
          tx.recurringId === template.id &&
          tx.date?.startsWith(ym)
        );

        if (!exists) {
          const daysInMonth = new Date(year, month, 0).getDate();
          const day = Math.min(templateDay, daysInMonth);
          const dateStr = `${ym}-${String(day).padStart(2, '0')}`;
          toAdd.push({
            ...template,
            id: `tx_auto_${template.id}_${ym}`,
            date: dateStr,
            autoGenerated: true,
            createdAt: new Date().toISOString()
          });
        }

        month++;
        if (month > 12) { month = 1; year++; }
      }
    }

    return toAdd;
  }, []);

  // Load from Supabase on mount — skip merge if pending import (local is authoritative)
  useEffect(() => {
    const pendingImport = localStorage.getItem('_pendingImportSync') === 'true';

    loadFromSupabase(SYNC_KEY).then(cloudData => {
      syncReady.current = true;
      if (pendingImport) {
        // After import: local is authoritative, push to Supabase and clear flag only on success
        setTransactions(prev => {
          saveToSupabase(SYNC_KEY, prev).then(ok => {
            if (ok) localStorage.removeItem('_pendingImportSync');
          });
          return prev;
        });
        return;
      }
      setTransactions(prev => {
        const merged = deduplicateByContent(mergeArrayById(prev, cloudData));
        const generated = generateRecurringInstances(filterActive(merged));
        const final = generated.length > 0 ? [...merged, ...generated] : merged;
        StorageManager.save(SYNC_KEY, final);
        return final;
      });
    });
  }, [generateRecurringInstances]);

  // Save to localStorage + Supabase on every change (skip initial mount to avoid overwriting before merge)
  const initialMount = useRef(true);
  useEffect(() => {
    if (initialMount.current) { initialMount.current = false; return; }
    StorageManager.save(SYNC_KEY, transactions);
    if (syncReady.current) saveToSupabase(SYNC_KEY, transactions);
  }, [transactions]);

  // ✅ Agregar transacción
  const addTransaction = useCallback((transaction) => {
    const id = `tx_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;
    const newTransaction = {
      id,
      date: transaction.date || new Date().toISOString().split('T')[0],
      description: transaction.description || 'Sin descripción',
      amount: parseFloat(transaction.amount) || 0,
      currency: transaction.currency || 'EUR',
      categoryId: transaction.categoryId || null,
      paymentMethod: transaction.paymentMethod || 'Tarjeta',
      notes: transaction.notes || '',
      imported: transaction.imported || false,
      importedAt: transaction.importedAt || null,
      recurrence: transaction.recurrence || null,
      recurringId: transaction.recurrence ? id : null,
      autoGenerated: false,
      createdAt: new Date().toISOString(),
      user: transaction.user || currentUser
    };

    setTransactions(prev => [...prev, newTransaction]);
    return true;
  }, [currentUser]);

  // ✅ Agregar transacciones en batch
  const addTransactionsBatch = useCallback((transactionsArray) => {
    if (!Array.isArray(transactionsArray) || transactionsArray.length === 0) {
      return { success: false, count: 0 };
    }

    const timestamp = Date.now();
    
    const newTransactions = transactionsArray.map((tx, index) => ({
      id: `tx_${timestamp}_${index}_${Math.random().toString(36).substr(2, 9)}`,
      date: tx.date || new Date().toISOString().split('T')[0],
      description: tx.description || 'Sin descripción',
      amount: parseFloat(tx.amount) || 0,
      currency: tx.currency || 'EUR',
      categoryId: tx.categoryId || null,
      paymentMethod: tx.paymentMethod || 'Tarjeta',
      notes: tx.notes || '',
      imported: tx.imported || false,
      importedAt: tx.importedAt || null,
      createdAt: new Date().toISOString(),
      user: tx.user || currentUser
    }));

    setTransactions(prev => [...prev, ...newTransactions]);
    return { success: true, count: newTransactions.length };
  }, [currentUser]);

  // ✅ Actualizar transacción
  const updateTransaction = useCallback((transactionId, updates) => {
    setTransactions(prev => prev.map(tx => 
      tx.id === transactionId 
        ? { ...tx, ...updates, updatedAt: new Date().toISOString() }
        : tx
    ));
    return true;
  }, []);

  // ✅ Eliminar transacción (soft-delete for multi-device sync)
  const deleteTransaction = useCallback((transactionId) => {
    setTransactions(prev => softDelete(prev, transactionId));
    return true;
  }, []);

  // ✅ Limpiar todas
  const clearAllTransactions = useCallback(() => {
    setTransactions([]);
    return true;
  }, []);

  // Active transactions (excludes soft-deleted for UI)
  const activeTransactions = useMemo(() => filterActive(transactions), [transactions]);

  // ✅ M25: Índice pre-calculado por mes y categoría
  const transactionIndex = useMemo(() => {
    const index = {
      byMonth: {},
      byCategory: {},
      byMonthAndCategory: {}
    };

    activeTransactions.forEach(tx => {
      const month = tx.date?.slice(0, 7);
      const catId = tx.categoryId;

      // Por mes
      if (month) {
        if (!index.byMonth[month]) index.byMonth[month] = [];
        index.byMonth[month].push(tx);
      }

      // Por categoría
      if (catId) {
        if (!index.byCategory[catId]) index.byCategory[catId] = [];
        index.byCategory[catId].push(tx);
      }

      // Por mes Y categoría
      if (month && catId) {
        const key = `${month}:${catId}`;
        if (!index.byMonthAndCategory[key]) index.byMonthAndCategory[key] = [];
        index.byMonthAndCategory[key].push(tx);
      }
    });

    return index;
  }, [activeTransactions]);

  // ✅ M25: Obtener transacciones por mes (O(1) en vez de O(n))
  const getTransactionsByMonth = useCallback((month) => {
    return transactionIndex.byMonth[month] || [];
  }, [transactionIndex]);

  // ✅ M25: Obtener transacciones por categoría y mes (O(1))
  const getTransactionsByCategoryAndMonth = useCallback((categoryId, month) => {
    const key = `${month}:${categoryId}`;
    return transactionIndex.byMonthAndCategory[key] || [];
  }, [transactionIndex]);

  const value = useMemo(() => ({
    transactions: activeTransactions,
    setTransactions,
    addTransaction,
    addTransactionsBatch,
    updateTransaction,
    deleteTransaction,
    clearAllTransactions,
    // M25: Funciones optimizadas
    transactionIndex,
    getTransactionsByMonth,
    getTransactionsByCategoryAndMonth
  }), [
    activeTransactions,
    addTransaction,
    addTransactionsBatch,
    updateTransaction,
    deleteTransaction,
    clearAllTransactions,
    transactionIndex,
    getTransactionsByMonth,
    getTransactionsByCategoryAndMonth
  ]);

  return (
    <TransactionsContext.Provider value={value}>
      {children}
    </TransactionsContext.Provider>
  );
}